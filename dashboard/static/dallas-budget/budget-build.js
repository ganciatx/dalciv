  function groupSum(rows, keyFn, valFn) {
    const acc = {};
    rows.forEach(r => {
      const k = keyFn(r);
      if (!acc[k]) acc[k] = { key: k, val: 0, rows: [] };
      acc[k].val += valFn(r);
      acc[k].rows.push(r);
    });
    return acc;
  }

